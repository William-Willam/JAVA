/*
 * ==================================================
 *              ESTRUTURA CONDICIONAL
 * ==================================================
 *
 *
 * 1 - EXPRESSÕES COMPARATIVAS
 * --------------------------------------------------
 *
 * Uma expressão comparativa é uma comparação
 * entre valores.
 *
 * O resultado dessa comparação será sempre:
 *
 *   - verdadeiro (true)
 *   - falso (false)
 *
 *
 * Exemplo:
 *
 *   5 > 10
 *
 * Resultado:
 *
 *   false
 *
 *
 * ==================================================
 *          OPERADORES COMPARATIVOS
 * ==================================================
 *
 * Os operadores comparativos são utilizados
 * para comparar valores.
 *
 *
 * Operadores:
 *
 *   >   maior que
 *   <   menor que
 *   >=  maior ou igual
 *   <=  menor ou igual
 *   ==  igual
 *   !=  diferente
 *
 *
 * Exemplos:
 *
 *   int x = 5;
 *
 *   x > 0
 *   Resultado: true
 *
 *
 *   x == 3
 *   Resultado: false
 *
 *
 *   10 <= 30
 *   Resultado: true
 *
 *
 *   x != 2
 *   Resultado: true
 *
 *
 * ==================================================
 *              EXPRESSÕES LÓGICAS
 * ==================================================
 *
 * Expressões lógicas servem para combinar
 * duas ou mais condições.
 *
 * O resultado também será:
 *
 *   - verdadeiro (true)
 *   - falso (false)
 *
 *
 * Operadores lógicos:
 *
 *
 * && -> E (AND)
 * --------------------------------------------------
 * As duas expressões precisam ser verdadeiras.
 *
 * Exemplo:
 *
 *   Suponha:
 *
 *   int x = 5;
 *
 *   x <= 20 && x == 10
 *
 * Resultado:
 *
 *   false
 *
 *
 * || -> OU (OR)
 * --------------------------------------------------
 * Apenas uma das expressões precisa ser verdadeira.
 *
 * Exemplo:
 *
 *   Suponha:
 *
 *   int x = 5;
 *
 *   x == 10 || x <= 20
 *
 * Resultado:
 *
 *   true
 *
 *
 * ! -> NÃO (NOT)
 * --------------------------------------------------
 * Inverte o resultado da condição.
 *
 * Exemplo:
 *
 *   Suponha:
 *
 *   int x = 5;
 *
 *   !(x == 10)
 *
 * Resultado:
 *
 *   true
 *
 */